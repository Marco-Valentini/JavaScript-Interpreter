let x = 10;
let y = 5;

console.log(x + y);  // Addition
console.log(x - y);  // Subtraction
console.log(x * y);  // Multiplication
console.log(x / y);  // Division

// examples of type coercion */
console.log("10" + 5);  // "105"
console.log("10" - 5);  // 5
console.log("10" * 5);  // 50
console.log("10" / 5);  // 2

/* examples of NaN */
console.log("hello" + 5);  // "hello5"
console.log("hello" - 5);  // NaN
console.log("hello" * 5);  // NaN
console.log("hello" / 5);  // NaN